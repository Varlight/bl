async function createNFT() {
  const nftMarketplaceContract = new ethers.Contract(contractAddress, abi, wallet);

  const tokenURI = "gbsdbsdgf"; 
  const price = ethers.utils.parseUnits("0.05", "ether"); // Example: 0.05 ETH

  // Note: You'll need to pay the listPrice when creating an NFT
  const tx = await nftMarketplaceContract.createListedToken(tokenURI, price, { 
      value: ethers.utils.parseUnits("0.01", "ether") // Example: Assuming listPrice is 0.01 ETH
  });

  await tx.wait();

  console.log(`NFT created and listed! Transaction hash: ${tx.hash}`);
}

